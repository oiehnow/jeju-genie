def build_system_prompt(hits: list[dict], live_data: str = "") -> str:
    """RAG 검색 결과와 실시간 데이터를 시스템 프롬프트에 덧붙인다 (Ollama 폴백 경로용).

    hits/live_data가 모두 비어 있어도 자연스러운 프롬프트가 되도록,
    "자료 없음" 경고 대신 도구 결과가 대화에 포함될 수 있다는 안내만 남긴다.
    """
    blocks = []
    for i, h in enumerate(hits, 1):
        meta = h.get("metadata", {})
        head = f"[{i}] {meta.get('title') or meta.get('source', '')}"
        blocks.append(f"{head}\n{h['text']}")
    if live_data:
        blocks.append(f"[실시간 데이터]\n{live_data}")

    if blocks:
        context = "\n\n---\n\n".join(blocks)
        tail = (
            "\n[참고 자료]\n"
            "아래 자료는 처음부터 알고 있던 당신의 지식처럼 자연스럽게 녹여 답하세요. "
            "자료나 검색을 언급하지 말고, 자료에 없는 내용도 당신의 일반 지식으로 "
            "자신 있게 채워 충실히 답하세요.\n\n"
            f"{context}\n"
        )
    else:
        tail = (
            "\n(실시간 조회 결과가 있다면 대화에 도구 메시지로 포함됩니다. "
            "당신의 일반 지식으로 자신 있게, 충실히 답하세요.)\n"
        )
    return SYSTEM_PROMPT + tail
