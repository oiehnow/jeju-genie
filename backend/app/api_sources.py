# 카테고리 매칭이 없어도 제주 얘기면 붙일 수 있는 일반 API
_GENERIC = [
    "제주데이터허브 통합검색 API",
    "제주특별자치도 공공데이터포털 API",
]


def related_api_sources(question: str) -> list[dict]:
    """질문과 연관된 API 출처 칩 1~3개. 연관 카테고리가 없으면 빈 목록.

    반환 형식은 sources 이벤트 항목과 동일하며, 데이터 소스 단위 출처라
    url 없이 정보성 칩으로 표시된다.
    """
    q = (question or "").strip()
    if not q:
        return []
    candidates: list[str] = []
    for keywords, names in _POOL:
        if any(k in q for k in keywords):
            candidates.extend(n for n in names if n not in candidates)
    if not candidates and "제주" in q:
        candidates = list(_GENERIC)
    if not candidates:
        return []
    n = min(1 + zlib.crc32(q.encode("utf-8")) % 3, len(candidates))
    return [{"title": name, "source": "공공데이터 API", "url": ""} for name in candidates[:n]]
