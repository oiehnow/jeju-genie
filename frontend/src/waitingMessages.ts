/**
 * 해당 테마 풀에서 직전 멘트(exclude)를 제외하고 랜덤으로 하나 고른다.
 * 모르는 테마는 generic으로 처리한다.
 */
export function pickMessage(theme: string, exclude?: string): string {
  const pool = WAITING_MESSAGES[theme] ?? WAITING_MESSAGES.generic;
  const candidates = pool.length > 1 && exclude ? pool.filter((m) => m !== exclude) : pool;
  return candidates[Math.floor(Math.random() * candidates.length)];
}
